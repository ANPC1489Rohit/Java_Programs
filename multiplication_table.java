
public class multiplication_table 
{

	 public static void main(String[] args) {

	        int num = 24;
	        int num1 = 50;
	        int num2 = 29;
	        
	        System.out.println("Multiplication of 24:");
	        for(int i = 1; i <= 10; ++i)
	        {
	            System.out.printf("%d * %d = %d \n", num, i, num * i);
	        }
	        
	        
	        System.out.println("Multiplication of 50:");
	        for(int i = 1; i <= 10; ++i)
	        {
	            System.out.printf("%d * %d = %d \n", num1, i, num1 * i);
	        }
	       
	        
	        System.out.println("Multiplication of 29:");
	        for(int i = 1; i <= 10; ++i)
	        {
	            System.out.printf("%d * %d = %d \n", num2, i, num2 * i);
	        }
	        
	    }
	
}
